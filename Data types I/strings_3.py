# Concatenating strings (multiline)

course = "Intro " \
         "to " \
         "Python"

greetings = (
    "Hello, "
    "my name is "
    "Andre"
)

docstring = """
     Welcome
          to
    Jurassic
        Park
"""

print(course)
print(greetings)
print(docstring)                                                                # Docstrings preserve both spaces and line breaks
                                                                                # It works like the 'pre' tag in HTML
